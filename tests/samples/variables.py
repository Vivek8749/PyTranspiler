# Variable declarations and arithmetic
x: int = 10
y: float = 3.14
name: str = "Alice"
flag: bool = True

# Arithmetic
a = x + 5
b = y * 2.0
c = x % 3

# Augmented assignment
x += 1
y -= 0.5
a *= 2

# Boolean logic
result = flag and True
check = x > 5 or y < 1.0

# Multiple assignment
p, q = 100, 200

print("x =", x)
print("y =", y)
print("name =", name)
print("a =", a)
print("p =", p, "q =", q)
